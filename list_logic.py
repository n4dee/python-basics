# List Logic Program
# Finding maximum, minimum, and count of elements in a list

numbers = [45, 60, 72, 88, 55]

# Count of elements
count = len(numbers)

# Finding maximum value
maximum = max(numbers)

# Finding minimum value
minimum = min(numbers)

print("List:", numbers)
print("Count of elements:", count)
print("Maximum value:", maximum)
print("Minimum value:", minimum)
